import { ResultsCard } from "@/components/results-card"
import { ResultsView } from "@/components/results-view"
import { Navbar } from "@/components/navbar"
import Link from "next/link"
import { getScanResult } from "@/lib/n8n"

type PageProps = { searchParams: Promise<{ executionId?: string }> }

export default async function ResultsPage({ searchParams }: PageProps) {
  const params = await searchParams
  const executionId = params.executionId ?? null

  // Server-side result only for demo or n8n execution ID; webhook (wh-xxx) is fetched client-side
  const serverResult = executionId && !executionId.startsWith("wh-") ? await getScanResult(executionId) : null
  const useClientFetch = executionId && (executionId.startsWith("wh-") || !serverResult)

  return (
    <main className="min-h-svh flex flex-col bg-background">
      {/* Header */}
      <header className="pt-8 pb-4 px-6">
        <div className="flex items-center gap-3">
          <Link 
            href="/"
            className="p-2 -ml-2 rounded-lg hover:bg-secondary transition-colors"
            aria-label="Go back"
          >
            <svg 
              className="w-6 h-6 text-foreground" 
              viewBox="0 0 24 24" 
              fill="none" 
              stroke="currentColor" 
              strokeWidth="2" 
              strokeLinecap="round" 
              strokeLinejoin="round"
            >
              <path d="M19 12H5M12 19l-7-7 7-7" />
            </svg>
          </Link>
          <h1 className="text-xl font-semibold text-foreground">
            Your Results
          </h1>
        </div>
      </header>

      {/* Main content */}
      <div className="flex-1 flex flex-col items-center px-4 pb-28 pt-4">
        {useClientFetch && executionId ? (
          <ResultsView executionId={executionId} />
        ) : serverResult ? (
          <ResultsCard
            checklist={serverResult.checklist}
            audioSrc={serverResult.audioUrl}
            audioBase64={serverResult.audio_base64}
          />
        ) : (
          <div className="w-full max-w-md py-12 text-center text-muted-foreground">
            <p>No results. Scan a doctor&apos;s note from the home page.</p>
          </div>
        )}

        {/* Link chat to this report */}
        {executionId && (
          <Link
            href={`/chat?executionId=${encodeURIComponent(executionId)}`}
            className="mt-6 w-full max-w-md flex items-center justify-center gap-2 rounded-xl border border-border bg-card px-4 py-3 text-sm font-medium text-foreground hover:bg-muted transition-colors"
          >
            <svg className="w-5 h-5 shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden>
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 12h.01M12 12h.01M16 12h.01M21 12c0 4.418-4.03 8-9 8a9.864 9.864 0 01-4.255-.949L3 20l1.395-3.72C3.512 15.042 3 13.574 3 12c0-4.418 4.03-8 9-8s9 3.582 9 8z" />
            </svg>
            Ask about this report
          </Link>
        )}

        {/* Additional info */}
        <p className="mt-6 text-sm text-muted-foreground text-center max-w-xs">
          This summary was generated by AI. Always consult your healthcare provider for medical advice.
        </p>
      </div>

      {/* Bottom navbar */}
      <Navbar />
    </main>
  )
}
